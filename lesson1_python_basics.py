# Printing in Python

# We Can use double quotations " "
print("Hello, World!")

# We Can also use single quotations ' '
print('Hello, World!')

# But what if the string includes single quotation/s ?
# We use double quotation for strings with single quotations:
print("Don't study")

# But what if the string includes double quotation/s ?
# We use single quotation for strings with double quotations:
print('This is a quote: "Quote"')

# We can also use triple (double quotations) """ """
print("""Something""")
